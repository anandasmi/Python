a = 10
b = 5
ans = bin(a & b)
print(ans)
# The bin() function is used to convert the result of the bitwise AND operation (0000) into a binary string. So, ans will be assigned the string '0b0', where '0b' is a prefix indicating that the following characters represent a binary number.

# Finally, the code prints the value of ans, which will be '0b0'.
